<h3 id="h-1-menjaga-kesehatan-pencernaan"><strong>1. Menjaga Kesehatan Pencernaan</strong></h3>



<p>Mangga dapat membantu menjaga kesehatan pencernaan karena kandungan senyawa amilase dan serat makanan yang mencegah sembelit. Senyawa amilase dapat membantu melarutkan makanan lain di perut, memecah pati yang sulit, sehingga mencegah sembelit.</p>



<h3 id="h-2-buah-mangga-mampu-meningkatkan-imun-tubuh"><strong>2. Buah Mangga Mampu Meningkatkan Imun Tubuh&nbsp;</strong></h3>



<p>Mangga mengandung nutrisi yang dapat meningkatkan kekebalan tubuh. Ini dikarenakan kandungan vitamin A yang dapat berfungsi menurunkan risiko infeksi.</p>



<p>Vitamin C pada mangga juga dapat membantu tubuh menghasilkan lebih banyak sel darah putih untuk melawan penyakit, membantu sel-sel tubuh bekerja lebih efektif, dan meningkatkan pertahanan kulit.</p>



<h3 id="h-3-menurunkan-risiko-kanker"><strong>3. Menurunkan Risiko Kanker</strong></h3>



<p>Buah mangga mengandung polifenol yang memiliki sifat antikanker. Polifenol berperan sebagai pelindung dari stres oksidatif, yang terkait dengan banyak jenis kanker.</p>



<p>Kandungan polifenol pada mangga dapat mengurangi stres oksidatif, menghancurkan atau menghentikan pertumbuhan berbagai sel kanker, termasuk leukemia dan kanker usus besar.</p>



<h3 id="h-4-mencegah-risiko-diabetes"><strong>4. Mencegah Risiko Diabetes</strong></h3>



<p>Mengonsumsi buah dan sayuran yang tinggi vitamin C dan karotenoid dapat membantu mencegah timbulnya diabetes. Mangga kaya akan kedua nutrisi ini, sehingga dapat memberikan manfaat yang serupa.&nbsp;</p>



<p>Meski begitu, mungkin dibutuhkan penelitian lebih jauh terkait hal ini. Pasalnya, buah mangga mengandung gula alami tinggi, sehingga berpotensi menyebabkan lonjakan kadar gula darah jika dimakan terlalu banyak.</p>



<h3 id="h-5-menjaga-kesehatan-jantung"><strong>5. Menjaga Kesehatan Jantung</strong></h3>



<p>Mengonsumsi buah mangga dapat menjaga kesehatan jantung. Ini dikarenakan mangga mengandung magnesium dan potasium, yang dapat membantu menjaga aliran darah tetap sehat.</p>



<p>Nutrisi ini sekaligus membantu pembuluh darah tetap rileks dan menstabilkan tekanan darah. Mangiferin sebagai antioksidan dari mangga juga baik untuk kesehatan jantung.&nbsp;&nbsp;</p>



<p>Mangiferin dapat melindungi sel-sel jantung dari peradangan, stres oksidatif, dan kematian sel. Selain itu, mangiferin juga dapat membantu menurunkan kadar kolesterol, trigliserida, dan asam lemak dalam darah .</p>



